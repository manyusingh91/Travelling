// Create Route
app.post("/listings", async (req, res) => {
  const { title, description, price, country, location, imageUrl } = req.body.listing;

  // Set a default image URL if no image URL is provided
  const image = imageUrl || 'https://images.unsplash.com/photo-1625505826533-5c80aca7d157?ixlib=rb-4.0.3&ixid=M3wxMjA3fDB8MHxzZWFyY2h8MTJ8fGdvYXxlbnwwfHwwfHx8MA%3D%3D&auto=format&fit=crop&w=800&q=60';

  const newListing = new Listing({
    title,
    description,
    price,
    country,
    location,
    image: { url: image }, 
  });

  await newListing.save();
  res.redirect("/listings");
});



//Edit Route
app.get("/listings/:id/edit", async (req, res) => {
  let { id } = req.params;
  const listing = await Listing.findById(id);
  console.log(listing);
  res.render("listings/edit.ejs", { listing });
});

app.put("/listings/:id", async (req, res) => {
  let { id } = req.params;
  const { title, description, price, country, location, imageUrl } = req.body.listing;

  // Check if imageUrl exists, if not, use a default image
  const updatedData = {
    title,
    description,
    price,
    country,
    location,
    image: imageUrl ? { url: imageUrl } : { url: 'https://images.unsplash.com/photo-1625505826533-5c80aca7d157?ixlib=rb-4.0.3&ixid=M3wxMjA3fDB8MHxzZWFyY2h8MTJ8fGdvYXxlbnwwfHwwfHx8MA%3D%3D&auto=format&fit=crop&w=800&q=60' },
  };

  // Update the listing
  await Listing.findByIdAndUpdate(id, updatedData);
  res.redirect(`/listings/${id}`);
});




// //Delete Route
app.delete("/listings/:id", async (req, res) => {
  let { id } = req.params;
  let deletedListing = await Listing.findByIdAndDelete(id);
  console.log(deletedListing);
  res.redirect("/listings");
});

